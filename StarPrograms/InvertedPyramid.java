package StarPrograms;

public class InvertedPyramid {
	public static void main(String args[]) {
		int i,j,k;
		for(i=1;i<=10;i++) {
			for(j=1;j<=i;j++) {
				System.out.print(" ");
			}
			for(k=10;k>=(i*2);k--) {
				System.out.print("*");
			}
			System.out.println(" ");
			
				
				
			
			
		}
	}

}
